"""
thumbnail_creator.py
Creates a visually rich YouTube thumbnail using Pillow.
Falls back gracefully if Pillow is not available.
"""
import os
import textwrap

try:
    from PIL import Image, ImageDraw, ImageFont
    PIL_AVAILABLE = True
except ImportError:
    PIL_AVAILABLE = False


def create_thumbnail(title: str, topic: str = "", output_path: str = "thumbnail.jpg") -> str:
    """
    Generate a simple but clean YouTube thumbnail (1280×720).
    Returns the path to the saved image.
    """
    width, height = 1280, 720

    if not PIL_AVAILABLE:
        # Create a minimal placeholder using basic bytes if Pillow is missing
        _create_placeholder(output_path, width, height)
        return output_path

    # ── Background gradient ────────────────────────────────────────────────────
    img = Image.new("RGB", (width, height), color=(15, 15, 35))
    draw = ImageDraw.Draw(img)

    # Draw gradient overlay
    for y in range(height):
        r = int(15 + (30 - 15) * y / height)
        g = int(15 + (20 - 15) * y / height)
        b = int(35 + (60 - 35) * y / height)
        draw.line([(0, y), (width, y)], fill=(r, g, b))

    # ── Accent bar on left ─────────────────────────────────────────────────────
    draw.rectangle([0, 0, 12, height], fill=(0, 180, 255))

    # ── Topic badge ────────────────────────────────────────────────────────────
    topic_text = topic.upper()[:20] if topic else "AI VIDEO"
    badge_padding = 20
    draw.rectangle(
        [60, 60, 60 + len(topic_text) * 18 + badge_padding * 2, 110],
        fill=(0, 180, 255)
    )
    try:
        badge_font = ImageFont.truetype("/System/Library/Fonts/Helvetica.ttc", 32)
    except Exception:
        badge_font = ImageFont.load_default()
    draw.text((60 + badge_padding, 68), topic_text, font=badge_font, fill=(255, 255, 255))

    # ── Main title ─────────────────────────────────────────────────────────────
    try:
        title_font = ImageFont.truetype("/System/Library/Fonts/Helvetica.ttc", 72)
        sub_font = ImageFont.truetype("/System/Library/Fonts/Helvetica.ttc", 38)
    except Exception:
        title_font = ImageFont.load_default()
        sub_font = ImageFont.load_default()

    # Word-wrap the title to fit within the thumbnail
    wrapped = textwrap.wrap(title, width=22)
    y_text = 160
    for line in wrapped[:3]:  # Max 3 lines
        draw.text((60, y_text), line, font=title_font, fill=(255, 255, 255))
        y_text += 85

    # ── Bottom bar ─────────────────────────────────────────────────────────────
    draw.rectangle([0, height - 80, width, height], fill=(0, 0, 0, 180))
    draw.text((60, height - 60), "AI Generated  •  Subscribe for more", font=sub_font, fill=(180, 180, 180))

    # ── Decorative dots ────────────────────────────────────────────────────────
    for i, (cx, cy, r, color) in enumerate([
        (1100, 150, 80, (0, 180, 255, 30)),
        (1200, 400, 120, (100, 50, 200, 20)),
        (950, 550, 60, (0, 220, 150, 25)),
    ]):
        overlay = Image.new("RGBA", (width, height), (0, 0, 0, 0))
        ov_draw = ImageDraw.Draw(overlay)
        ov_draw.ellipse([cx - r, cy - r, cx + r, cy + r], fill=color)
        img = Image.alpha_composite(img.convert("RGBA"), overlay).convert("RGB")
        draw = ImageDraw.Draw(img)

    img.save(output_path, "JPEG", quality=95)
    return output_path


